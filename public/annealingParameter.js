class ArrayQuery{
    constructor(data){
        this.data = data
    }

    find(key, comparator, value){
        let fil = this.data.filter((obj) => {
            switch(comparator){
                case "GT":
                case ">":
                    return obj[key] > value;
                    break;
                case "GTE":
                case ">=":
                    return obj[key] >= value;
                    break;
                case "LT":
                case "<":
                    return obj[key] < value;
                    break;
                case "LTE":
                case "<=":
                    return obj[key] <= value;
                    break;
                case "EQ":
                case "==":
                default:
                    return obj[key] == value;
            }
        });

        return new ArrayQuery(fil);
    }
}

